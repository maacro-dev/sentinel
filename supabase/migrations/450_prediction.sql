create or replace function public.get_available_locations_for_predictions(p_season_id int default null)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
    target_season_id int;
    result jsonb;
begin
    if p_season_id is null then
        select id into target_season_id
        from public.seasons
        order by start_date desc
        limit 1;
    else
        target_season_id := p_season_id;
    end if;

    with valid_predictions as (
        select distinct
            f.mfid_id,
            fa.season_id
        from public.predicted_yields py
        join public.fields f on py.mfid_id = f.mfid_id
        join public.field_activities fa on f.id = fa.field_id and fa.season_id = py.season_id and fa.activity_type = 'field-data'
        join public.field_plannings fp on fa.id = fp.id
        left join lateral (
            select ce.* from public.crop_establishments ce
            join public.field_activities fa2 on ce.id = fa2.id
            where fa2.field_id = f.id and fa2.season_id = py.season_id and fa2.activity_type = 'cultural-management'
            limit 1
        ) ce on true
        where fa.season_id = target_season_id
          and fp.est_crop_establishment_date is not null
          and ce.rice_variety_maturity_duration is not null
    ),
    province_list as (
        select distinct p.name
        from valid_predictions vp
        join public.fields f on vp.mfid_id = f.mfid_id
        join public.addresses a on f.barangay_id = a.barangay_id
        join public.provinces p on a.province_id = p.id
    ),
    municipality_list as (
        select distinct cm.name, p.name as province_name
        from valid_predictions vp
        join public.fields f on vp.mfid_id = f.mfid_id
        join public.addresses a on f.barangay_id = a.barangay_id
        join public.cities_municipalities cm on a.city_municipality_id = cm.id
        join public.provinces p on a.province_id = p.id
    ),
    barangay_list as (
        select distinct b.name, cm.name as municipality_name
        from valid_predictions vp
        join public.fields f on vp.mfid_id = f.mfid_id
        join public.addresses a on f.barangay_id = a.barangay_id
        join public.barangays b on a.barangay_id = b.id
        join public.cities_municipalities cm on a.city_municipality_id = cm.id
    )
    select jsonb_build_object(
        'provinces', coalesce((select jsonb_agg(name order by name) from province_list), '[]'::jsonb),
        'municipalities', coalesce((select jsonb_agg(jsonb_build_object('name', name, 'province', province_name) order by province_name, name) from municipality_list), '[]'::jsonb),
        'barangays', coalesce((select jsonb_agg(jsonb_build_object('name', name, 'municipality', municipality_name) order by municipality_name, name) from barangay_list), '[]'::jsonb)
    ) into result;

    return result;
end;
$$;


CREATE OR REPLACE FUNCTION get_rice_varieties_for_predictions(
    p_season_id INT DEFAULT NULL,
    p_province TEXT DEFAULT NULL,
    p_municipality TEXT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql AS $$
BEGIN
    RETURN COALESCE(
        (SELECT jsonb_agg(DISTINCT ce.rice_variety ORDER BY ce.rice_variety)
         FROM public.crop_establishments ce
         JOIN public.field_activities fa ON ce.id = fa.id
         JOIN public.fields f ON fa.field_id = f.id
         JOIN public.addresses a ON f.barangay_id = a.barangay_id
         JOIN public.provinces p ON a.province_id = p.id
         JOIN public.cities_municipalities cm ON a.city_municipality_id = cm.id
         WHERE (p_season_id IS NULL OR fa.season_id = p_season_id)
           AND (p_province IS NULL OR p.name = p_province)
           AND (p_municipality IS NULL OR cm.name = p_municipality)),
        '[]'::JSONB
    );
END;
$$;

CREATE OR REPLACE FUNCTION get_soil_types_for_predictions(
    p_season_id INT DEFAULT NULL,
    p_province TEXT DEFAULT NULL,
    p_municipality TEXT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql AS $$
BEGIN
    RETURN COALESCE(
        (SELECT jsonb_agg(DISTINCT fp.soil_type ORDER BY fp.soil_type)
         FROM public.field_plannings fp
         JOIN public.field_activities fa ON fp.id = fa.id
         JOIN public.fields f ON fa.field_id = f.id
         JOIN public.addresses a ON f.barangay_id = a.barangay_id
         JOIN public.provinces p ON a.province_id = p.id
         JOIN public.cities_municipalities cm ON a.city_municipality_id = cm.id
         WHERE (p_season_id IS NULL OR fa.season_id = p_season_id)
           AND (p_province IS NULL OR p.name = p_province)
           AND (p_municipality IS NULL OR cm.name = p_municipality)
           AND fp.soil_type IS NOT NULL),
        '[]'::JSONB
    );
END;
$$;
