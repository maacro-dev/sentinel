create table if not exists predicted_yields(
    id                      int generated by default as identity primary key,
    mfid_id                 int not null references mfids(id),
    season_id               int not null references seasons(id),
    predicted_yield_t_ha    double precision not null,
    generated_at            timestamptz not null default now(),
    unique (mfid_id, season_id));

create index idx_py_mfid_id on predicted_yields(mfid_id);

create index idx_py_season_id on predicted_yields(season_id);


