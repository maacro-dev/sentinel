
create table predicted_yields (
  id                    int generated by default as identity primary key,
  field_id              int not null references fields(id),
  season_id             int not null references seasons(id),
  predicted_yield_t_ha  double precision not null,
  generated_at          timestamptz not null default now(),

  unique (field_id, season_id)
);
create index idx_py_field_id on predicted_yields(field_id);
create index idx_py_season_id on predicted_yields(season_id);

